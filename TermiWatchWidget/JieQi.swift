import Foundation

func getJieQi(date: Date) -> String? {

    let calendar = Calendar(identifier: .gregorian)
    let dateComponents = calendar.dateComponents([.year, .month, .day], from: date)
    
    let iYear = dateComponents.year ?? 2023
    let iMonth = dateComponents.month ?? 12
    let iDay = dateComponents.day ?? 12
    
    let array_index = (iYear - START_YEAR) * 12 + iMonth - 1
    let flag = gLunarHolDay[array_index]
    var day = 0
    if iDay < 15 {
        day = 15 - ((flag >> 4)&0x0f)
    }else {
        day = ((flag)&0x0f) + 15
    }
    var index = -1
    if iDay == day {
        index = (iMonth-1) * 2 + (iDay > 15 ? 1: 0);
    }
    
    if index >= 0 && index < chineseDays.count {
        return chineseDays[index]
    }
    return nil
}
    
    
